// Indian numbering system: groups of two digits above the hundreds.
const SCALES = [
  [1_000_000_000_000_000, "पद्म"],
  [10_000_000_000_000, "नील"],
  [100_000_000_000, "खरब"],
  [1_000_000_000, "अरब"],
  [10_000_000, "करोड़"],
  [100_000, "लाख"],
  [1_000, "हज़ार"],
  [100, "सौ"],
] as const;

function toWordsAbs(n: number): string {
  if (n === 0) return UNITS[0];

  const parts: string[] = [];
  for (const [value, scale] of SCALES) {
    if (n >= value) {
      parts.push(`${UNITS[Math.floor(n / value)]} ${scale}`);
      n %= value;
    }
  }
  if (n > 0) parts.push(UNITS[n]!);
  return parts.join(" ");
}

/** Hindi: 42 → "बयालीस" (Indian system: 123456 → एक लाख तेईस हज़ार चार सौ छप्पन) */
export const hi: LocaleConverter = (num) =>
  (num < 0 ? "माइनस " : "") + toWordsAbs(Math.abs(num));
